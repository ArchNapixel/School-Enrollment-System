    private void assignbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignbuttonActionPerformed
        if (teachid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a teacher first.", "No Teacher Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        if (subjectsFormInstance == null) {
            JOptionPane.showMessageDialog(this, "Please open the Subjects form first.", "Subjects Form Not Open", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String selectedSubjID = subjectsFormInstance.getSelectedSubjectID();
        if (selectedSubjID == null || selectedSubjID.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a subject in the Subjects form first.", "No Subject Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int teacherID = Integer.parseInt(teachid.getText());
            int subjectID = Integer.parseInt(selectedSubjID);

            ColesEnrollmentSystem conn = new ColesEnrollmentSystem();
            conn.DBConnect();

            String checkQuery = "SELECT t.teachname FROM TeachersTable t "
                    + "INNER JOIN Assign a ON a.tid = t.teachid "
                    + "WHERE a.subid = ?";
            
            PreparedStatement ps = conn.con.prepareStatement(checkQuery);
            ps.setInt(1, subjectID);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                //String assignedTeacher = rs.getString("teachname");
                JOptionPane.showMessageDialog(this,
                        "Subject already assigned to: " + teacherID,
                        "Assignment Blocked",
                        JOptionPane.WARNING_MESSAGE);
                return;
            }

            String confirmMessage = "Assign Teacher ID: " + teacherID + " to Subject ID: " + subjectID;
            int choice = JOptionPane.showConfirmDialog(this, confirmMessage, "Confirm Assignment", JOptionPane.OK_CANCEL_OPTION);

            if (choice == JOptionPane.OK_OPTION) {
                assignHandler.setsubjid(subjectID);
                String result = assignHandler.AssignSubj(teacherID);

                if (result.contains("successfully")) {
                    JOptionPane.showMessageDialog(this, "Teacher: " + teacherID + " assigned to " + subjectID + ".", "Assignment Successful", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, result, "Assignment Failed", JOptionPane.ERROR_MESSAGE);
                }

                ShowAssignRec();
                subjectsFormInstance.ShowClassList(); 
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid teacher or subject ID.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error during assignment: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_assignbuttonActionPerformed

    private void deletesubjbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletesubjbuttonActionPerformed
        int selectedRow = assigntable.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Please select a subject to remove.", "No Subject Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        String teacherText = teachid.getText();
        if (teacherText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a teacher first.", "No Teacher Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int teacherID = Integer.parseInt(teacherText);
            int subjectID = Integer.parseInt(assigntable.getValueAt(selectedRow, 0).toString());

            ColesEnrollmentSystem conn = new ColesEnrollmentSystem();
            conn.DBConnect();

            String query = "SELECT * FROM Assign WHERE tid = ? AND subid = ?";
            PreparedStatement ps = conn.con.prepareStatement(query);
            ps.setInt(1, teacherID);
            ps.setInt(2, subjectID);
            ResultSet rs = ps.executeQuery();

            if (!rs.next()) {
                JOptionPane.showMessageDialog(this, "No matching assignment found for deletion.", "Not Found", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String confirm = "Delete Subject: " + subjectID + " assigned by Teacher: " + teacherID;
            int confirmChoice = JOptionPane.showConfirmDialog(this, confirm, "Confirm Delete Assignment", JOptionPane.OK_CANCEL_OPTION);

            if (confirmChoice == JOptionPane.OK_OPTION) {
                assignHandler.setsubjid(subjectID);
                String result = assignHandler.DeleteSubj(teacherID);

                if (result.contains("successfully")) {
                    JOptionPane.showMessageDialog(this, "Teacher: " + teacherID + " no longer assigned to Subject: " + subjectID, "Assignment Removed", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, result, "Deletion Failed", JOptionPane.ERROR_MESSAGE);
                }

                ShowAssignRec();

                if (subjectsFormInstance != null) {
                    subjectsFormInstance.showRecords();
                }
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid teacher or subject ID.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error during deletion: " + e.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_deletesubjbuttonActionPerformed
    private void setNextTeacherID() {
        try {
            ColesEnrollmentSystem conn = new ColesEnrollmentSystem();
            conn.DBConnect();

            String query = "SELECT MAX(teachid) FROM TeachersTable"; 
            PreparedStatement ps = conn.con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            int nextID = 3001;
            if (rs.next()) {
                int maxID = rs.getInt(1); 
                if (!rs.wasNull() && maxID >= 3001) {
                    nextID = maxID + 1;
                }
            }

            teachid.setText(String.valueOf(nextID));

            rs.close();
            ps.close();
            conn.con.close();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error generating next Teacher ID: " + ex.getMessage(), "ID Generation Error", JOptionPane.ERROR_MESSAGE);
            teachid.setText("3001");
        }
    }


