    public void showRecords() {
        DefaultTableModel tblmodel = (DefaultTableModel) Teachers.getModel();
        tblmodel.setRowCount(0);

        try {
            ColesEnrollmentSystem a = new ColesEnrollmentSystem();
            a.DBConnect();

            String query = "SELECT teachid, teachname, teachaddress, teachcontact, teachemail, teachdept FROM TeachersTable";
            PreparedStatement ps = a.con.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                String i = rs.getString("teachid");
                String j = rs.getString("teachname");
                String k = rs.getString("teachaddress");
                String l = rs.getString("teachcontact");
                String m = rs.getString("teachemail");
                String n = rs.getString("teachdept");

                tblmodel.addRow(new Object[]{i, j, k, l, m, n});
            }

            rs.close();
            ps.close();
            a.con.close();

            System.out.println("QUERY: " + query);

        } catch (Exception ex) {
            System.out.println("QUERY FAILED: " + ex.getMessage());
            ex.printStackTrace();
        }
    }

    
    private void ShowAssignRec() {
        if (teachid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a teacher first.", "No Teacher Selected", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            int teachID = Integer.parseInt(teachid.getText());

            if (assigntable.getModel() == null || !(assigntable.getModel() instanceof DefaultTableModel)) {
                assigntable.setModel(new DefaultTableModel(
                        new Object[][]{},
                        new String[]{"Subject ID", "Code", "Description", "Schedule", "Units"}
                ));
            }

            DefaultTableModel tblmodel = (DefaultTableModel) assigntable.getModel();
            tblmodel.setRowCount(0);

            ColesEnrollmentSystem conn = new ColesEnrollmentSystem();
            conn.DBConnect();

            String query = "SELECT s.subjid, s.subjcode, s.subjdescription, s.subjschedule, s.subjunit "
                    + "FROM SubjectsTable s "
                    + "INNER JOIN Assign a ON s.subjid = a.subid "
                    + "WHERE a.tid = ?";

            PreparedStatement ps = conn.con.prepareStatement(query);
            ps.setInt(1, teachID);
            ResultSet rs = ps.executeQuery();

            boolean hasResults = false;
            while (rs.next()) {
                hasResults = true;

                String subjid = rs.getString("subjid");
                String subjcode = rs.getString("subjcode");
                String subjdesc = rs.getString("subjdescription");
                String subjsched = rs.getString("subjschedule");
                String subjunit = rs.getString("subjunit");

                System.out.println("Fetched: " + subjid + ", " + subjcode + ", " + subjdesc + ", " + subjsched + ", " + subjunit);

                tblmodel.addRow(new Object[]{subjid, subjcode, subjdesc, subjsched, subjunit});
            }

            if (!hasResults) {
                JOptionPane.showMessageDialog(this, "No subjects assigned to teacher ID: " + teachID, "No Assignments", JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Invalid teacher ID format.", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error showing assigned subjects: " + ex.getMessage(), "Database Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }

    }
